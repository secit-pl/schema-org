<?php

namespace SecIT\SchemaOrg\Mapping\Property;

use SecIT\SchemaOrg\Mapping\DataType;
use SecIT\SchemaOrg\Mapping\Type;

/**
 * The base salary of the job or of an employee in an EmployeeRole.
 * 
 * @method BaseSalaryProperty setValue($value)
 */
class BaseSalaryProperty extends AbstractProperty {

	/**
	 * Get schema URL.
	 *
	 * @return string
	 */
	public function getSchemaUrl() {
		return 'https://schema.org/baseSalary';
	}

	/**
	 * Check is value valid.
	 *
	 * @param string $value
	 * @return bool
	 */
	public function isValueValid($value) {
		return $value instanceof Type\MonetaryAmountType || $value instanceof DataType\NumberType || $value instanceof Type\PriceSpecificationType;
	}
}