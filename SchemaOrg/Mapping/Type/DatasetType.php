	}

	/**
	 * Get schema URL.
	 * 
	 * @return string
	 */
	public function getSchemaUrl() {
		return 'https://schema.org/Dataset';
	}

	/**
	 * Set distribution.
	 * 
	 * @param Property\DistributionProperty $distribution
	 * @return DatasetType
	 */
