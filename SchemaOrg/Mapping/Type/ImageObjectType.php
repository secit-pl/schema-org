	/**
	 * @var Property\ExifDataProperty
	 */
	private $exifData;

	/**
	 * @var Property\RepresentativeOfPageProperty
	 */
	private $representativeOfPage;

	/**
	 * Get caption.
	 *
	 * @return Property\CaptionProperty
	 */
	public function getCaption() {
		return $this->caption;
	}

	/**
	 * Get exif data.
	 *
	 * @return Property\ExifDataProperty
	 */
	public function getExifData() {
		return $this->exifData;
	}

	/**
	 * Get representative of page.
	 *
	 * @return Property\RepresentativeOfPageProperty
	 */
	public function getRepresentativeOfPage() {
		return $this->representativeOfPage;
	}

	/**
	 * Get schema URL.
	 *
	 * @return string
	 */
	public function getSchemaUrl() {
		return 'https://schema.org/ImageObject';
	}

	/**
	 * Set caption.
	 *
	 * @param Property\CaptionProperty $caption
	 * @return ImageObjectType
	 */
	public function setCaption(Property\CaptionProperty $caption) {
		$this->caption = $caption;

		return $this;
	}

	/**
	 * Set exif data.
	 *
	 * @param Property\ExifDataProperty $exifData
	 * @return ImageObjectType
	 */
